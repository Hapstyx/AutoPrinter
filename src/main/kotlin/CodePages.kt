/**
 * Decodes a given string into bytes using a given pre-defined code page.
 * All code pages are supersets of ASCII, so if the text only contains ASCII
 * characters, the encoding does not matter.
 * @param text     the text to decode
 * @param codePage the code page to use for non-ASCII characters
 * @return a list of all characters in [text] decoded into bytes
 */
fun decodeWithCodeTable(text: String, codePage: CodePage): List<Byte> {
    val characterLookup = when (codePage) {
        CodePage.CP437  -> CP437
        CodePage.CP850  -> CP850
        CodePage.CP860  -> CP860
        CodePage.CP863  -> CP863
        CodePage.CP865  -> CP865
        CodePage.CP1252 -> CP1252
        CodePage.CP866  -> CP866
        CodePage.CP852  -> CP852
        CodePage.CP858  -> CP858
    }

    return text.map {
        if (it.code in 0..0x7F)
            it.code.toByte()
        else
            (characterLookup.indexOf(it.code) + 0x80).toByte()
    }
}
